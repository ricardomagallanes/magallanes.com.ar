let currentLang = 'es';

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;

    // Update labels
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const keys = key.split('.');
        let val = translations[lang];
        keys.forEach(k => {
            if (val) val = val[k];
        });
        if (val) el.textContent = val;
    });

    // Update SEO Meta Tags
    document.title = translations[lang].seo.title;
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute('content', translations[lang].seo.desc);

    // Update OG Tags
    const ogTitle = document.querySelector('meta[property="og:title"]');
    const ogDesc = document.querySelector('meta[property="og:description"]');
    if (ogTitle) ogTitle.setAttribute('content', translations[lang].seo.title);
    if (ogDesc) ogDesc.setAttribute('content', translations[lang].seo.desc);

    // Update Dropdown UI
    const currentLangLabel = document.getElementById('current-lang-label');
    const currentLangFlag = document.getElementById('current-lang-flag');

    if (currentLangLabel) currentLangLabel.textContent = langMeta[lang].name;
    if (currentLangFlag) {
        currentLangFlag.className = `fi fi-${langMeta[lang].flag}`;
    }

    // Save preference
    localStorage.setItem('preferred-lang', lang);

    // Update HTML lang attribute
    document.documentElement.lang = lang;

    // Close dropdown
    const dropdown = document.querySelector('.lang-dropdown');
    if (dropdown) dropdown.classList.remove('active');
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Flag Dropdown Toggle
    const dropdownBtn = document.querySelector('.lang-dropdown-btn');
    const dropdown = document.querySelector('.lang-dropdown');

    if (dropdownBtn && dropdown) {
        dropdownBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            dropdown.classList.toggle('active');
        });

        // Close when clicking outside
        document.addEventListener('click', () => {
            dropdown.classList.remove('active');
        });
    }

    // Individual Lang Selection
    document.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('click', () => {
            setLanguage(option.getAttribute('data-lang'));
        });
    });

    // Initial Lang
    const savedLang = localStorage.getItem('preferred-lang') || 'es';
    setLanguage(savedLang);
});
